// Declaração das variáveis
let booleanVar;
let bigintVar;
let stringVar;
let symbolVar;

// Exibindo os conteúdos iniciais (undefined)
console.log("Conteúdo inicial das variáveis:");
console.log("booleanVar:", booleanVar);
console.log("bigintVar:", bigintVar);
console.log("stringVar:", stringVar);
console.log("symbolVar:", symbolVar);

// Atribuindo valores nulos às variáveis
booleanVar = null;
bigintVar = null;
stringVar = null;
symbolVar = null;

// Exibindo as variáveis após atribuir valores nulos
console.log("\nVariáveis após atribuir valores nulos:");
console.log("booleanVar:", booleanVar);
console.log("bigintVar:", bigintVar);
console.log("stringVar:", stringVar);
console.log("symbolVar:", symbolVar);
